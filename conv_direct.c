#include "process.h"
#include "stdlib.h"
#include "arm_math.h"
#include "assert.h"
#include "conv_direct.h"

struct fir_t {
    arm_fir_instance_f32 instance;
    uint16_t numTaps;
    float *rpCoeffs;
    float *pState;
};

/*
 *
 */
fir_handle_t fir_init(const float *coeffs,
uint16_t coeffs_size, uint16_t block_size, uint16_t gain ) {

    // New fir handler
    fir_handle_t fir = (fir_handle_t) malloc(sizeof(fir_t));
    assert(fir_handle_t);
   
    // Create and assign state variable array
    fir->pState = (float*) malloc((coeffs_size * block_size - 1) * sizeof(float));
    assert(fir->pState);
    
    // 
    fir->numTaps = coeffs_size;

    // Reverse load fir coeffs
    fir->rpCoeffs = (float *) malloc((coeffs_size)*sizeof(float)); 
    assert(fir->rpCoeffs);
    for(uint16_t idx = 0; idx < fir->numTaps; idx++) {
        fir->rpCoeffs[idx] = gain * coeffs[coeffs_size - idx - 1];
    }

    // Init fir filter    
    arm_fir_init_f32(&(fir->instance),
    fir->numTaps, fir->rpCoeffs, fir->pState, block_size);

    return fir;
}

/*
 *
 */
void fir_update(fir_handle_t f,
 float *inp, float *outp, uint16_t block_size) {
    arm_fir_f32( &(f->instance),
 inp, outp, block_size);
}


